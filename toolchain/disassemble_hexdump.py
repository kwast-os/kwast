#!/usr/bin/env python3

import struct
import os
import tempfile

list = [0x55, 0x48, 0x89, 0xe5, 0x8b, 0x87, 0x34, 0x12, 0x0, 0x0, 0x83, 0xc0, 0x1, 0x89, 0x87, 0x34, 0x12, 0x0, 0x0, 0x5d, 0xc3]

out = tempfile.NamedTemporaryFile(mode = 'wb')
out.write(struct.pack(f'{len(list)}B', *list))
out.flush()
os.system(f'objdump -b binary -D -m i386:x86-64 {out.name}')
out.close()
