#!/usr/bin/env python3

import struct
import os
import tempfile

list = [0x55, 0x48, 0x89, 0xe5, 0xb8, 0xd2, 0x4, 0x0, 0x0, 0x40, 0x89, 0xc7, 0xe8, 0xef, 0xff, 0xff, 0xff, 0x5d, 0xc3]

out = tempfile.NamedTemporaryFile(mode = 'wb')
out.write(struct.pack(f'{len(list)}B', *list))
out.flush()
os.system(f'objdump -b binary -D -m i386:x86-64 {out.name}')
out.close()
